'''
Các hàm quan trọng trong xử lý Python
    - Phương thức chuỗi:
        s.lower(): Chuyển đổi tất cả các ký tự trong chuỗi thành chữ thường.
        s.upper(): Chuyển đổi tất cả các ký tự trong chuỗi thành chữ in hoa.
        s.title(): Chuyển đổi ký tự đầu tiên của mỗi từ thành chữ in hoa.
        s.strip(): Xóa khoảng trắng ở đầu và cuối chuỗi.
        s.split(delimiter): Tách chuỗi thành danh sách các chuỗi con dựa trên ký tự phân cách (mặc định là khoảng trắng).
        s.join(iterable): Nối các phần tử trong iterable thành một chuỗi, sử dụng chuỗi s làm ký tự phân cách.
        s.replace(old, new): Thay thế tất cả các xuất hiện của chuỗi old bằng chuỗi new.
        s.find(sub): Trả về chỉ số của lần xuất hiện đầu tiên của chuỗi sub, hoặc -1 nếu không tìm thấy.
        s.count(sub): Đếm số lần xuất hiện của chuỗi sub trong chuỗi.
        re.search(): được sử dụng để tìm kiếm một mẫu trong chuỗi ban đầu. Nó trả về một đối tượng match nếu tìm thấy mẫu, hoặc None nếu không tìm thấy.

'''